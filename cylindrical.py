radius = int(input('Please insert radius of Cylindrical :'))
height = int(input('Please insert height of Cylindrical :'))
vol = height * radius**2 * 3.14
print('Cylindrical vol. is:',vol) #hajme ostovaneh
area = 2 * 3.14 * radius * (height + 2) * (3.14 * radius**2)
print('Cylindrical area is:',area) #masahate ostovaneh

